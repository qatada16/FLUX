export function ProviderIcon({ providerKey, displayName, size = 44, color }: ProviderIconProps) {
  if (providerKey === 'jazzcash') {
    return (
      <View style={[styles.container, { width: size, height: size, borderRadius: size / 2, backgroundColor: '#000000', overflow: 'hidden' }]}>
        <JazzCashLogo size={size * 0.9} />
      </View>
    );
  }

  const logoImage = logoImages[providerKey];
  if (logoImage) {
    return (
      <View style={[styles.container, { width: size, height: size, borderRadius: size / 2, overflow: 'hidden' }]}>
        <Image
          source={logoImage}
          style={{ width: size, height: size }}
          resizeMode="cover"
        />
      </View>
    );
  }

  const bgColor = color || iconColors[providerKey] || '#555';
  // Get up to 2 initials from the display name
  const initials = displayName
    .split(' ')
    .map((w) => w[0])
    .join('')
    .slice(0, 2)
    .toUpperCase();

  return (
    <View style={[styles.container, { width: size, height: size, borderRadius: size / 2, backgroundColor: bgColor }]}>
      <Text style={[styles.initials, { fontSize: size * 0.36 }]}>{initials}</Text>
    </View>
  );
}

const styles = StyleSheet.create({
  container: {
    alignItems: 'center',
    justifyContent: 'center',
  },
  initials: {
    fontFamily: 'Sora_700Bold',
    color: '#FFFFFF',
  },
});
